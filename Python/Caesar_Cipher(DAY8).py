def caesar(text, shift, direction):
    output = ""
    if direction == 'decode':
        shift *= -1
    for letter in text:
        if letter in alphabet:
            ind = alphabet.index(letter)
            shifted_ind = ind + shift
            if shifted_ind > len(alphabet) - 1:
                shifted_ind %= len(alphabet)
            output += alphabet[shifted_ind]
        else:
            output += letter
    print(f"{direction.title()}d, here's your message: {output}")


